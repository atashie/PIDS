import numpy as np
import pytest

from pids_forward.physics.sorptive_closure import (
    F_cylindrical, F_throttle, throttle_params, sorptive_clock, rel_l2)

# ---- PRE-REGISTERED GATE CONSTANTS (locked 2026-06-10, before any embedded-scheme run) ------------
EMBEDDED_TOL = 0.10     # the embedded scheme must pass EVERY leg at <= this rel-L2, across n
BASELINE_KILL = 0.20    # every passive baseline must fail >= 1 leg by >= this (worst measured: 0.289)

R_W = 0.05
DISP = "tests/data/m4_phase4_refA_disperse.npz"
DISP_B = "tests/data/m4_phase4_refB_disperse.npz"
DRAIN = "tests/data/m4_phase4_refAB_drain.npz"


@pytest.fixture(scope="module")
def refs():
    return np.load(DISP), np.load(DISP_B), np.load(DRAIN)


def _planar(z):
    return np.ones_like(np.asarray(z, dtype=float))


def _clock(t, S, dth, F):
    return sorptive_clock(t, S, dth, R_W, F)


# ---- fixture sanity -------------------------------------------------------------------------------
def test_fixtures_sane(refs):
    a, b, d = refs
    for npz, soils, rs in ((a, ("LOAM", "SAND", "SILT"), (3, 5, 10)),
                           (d, ("LOAM", "SAND"), (3, 5, 10))):
        for s in soils:
            dth = float(npz[f"{s}_dtheta"])
            for k in rs:
                t, I, imax = npz[f"{s}_R{k}_t"], npz[f"{s}_R{k}_I"], float(npz[f"{s}_R{k}_Imax"])
                assert np.all(np.diff(t) > 0) and np.all(np.diff(I) >= -1e-12 * imax)
                assert I[-1] <= imax * (1 + 1e-9)
                assert abs(imax - dth * ((k * R_W) ** 2 - R_W ** 2) / (2 * R_W)) < 1e-12
    for npz, key in ((b, "LOAM"), (d, "LOAM_B")):
        t, I = npz[f"{key}_t"] if npz is d else npz["LOAM_t"], npz[f"{key}_I"] if npz is d else npz["LOAM_I"]
        assert np.all(np.diff(t) > 0) and np.all(np.diff(I) >= -1e-14)


