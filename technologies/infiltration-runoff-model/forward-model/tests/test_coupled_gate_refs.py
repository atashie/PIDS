# ---- drain legs (the clamp-family killer) ---------------------------------------------------------
def test_drain_raw_throttle_fails_refA(refs):
    _, _, d = refs
    for s in ("LOAM", "SAND"):
        S, dth = float(d[f"{s}_Sdes"]), float(d[f"{s}_dtheta"])
        z0, kk = throttle_params(dth)
        for k in (3, 5, 10):
            t, I = d[f"{s}_R{k}_t"], d[f"{s}_R{k}_I"]
            e = rel_l2(_clock(t, S, dth, lambda z: F_throttle(z, z0, kk)), I)
            assert e >= 0.25, f"{s} drain R{k}: throttle clock relL2 {e:.1%} -- discrimination lost"


def test_clamp_family_is_killed_by_the_drain_legs(refs):
    """Each passive drain variant (throttle / planar F=1 / cylindrical F, clamped or not) must fail
    at least one drain leg by >= BASELINE_KILL. Measured worst cases: throttle 28.9% (LOAM R3);
    planar 31.7% (LOAM R5); cyl 40.9% (LOAM R3). Clamping is a no-op or worse for under-prediction."""
    _, _, d = refs
    for s in ("LOAM", "SAND"):
        S, dth = float(d[f"{s}_Sdes"]), float(d[f"{s}_dtheta"])
        z0, kk = throttle_params(dth)
        for Fname, F in (("throttle", lambda z: F_throttle(z, z0, kk)),
                         ("planar", _planar), ("cyl", F_cylindrical)):
            worst = 0.0
            for k in (3, 5, 10):
                t, I, imax = d[f"{s}_R{k}_t"], d[f"{s}_R{k}_I"], float(d[f"{s}_R{k}_Imax"])
                clk = _clock(t, S, dth, F)
                worst = max(worst, min(rel_l2(clk, I), rel_l2(np.minimum(clk, imax), I)))
            assert worst >= BASELINE_KILL, \
                f"{s} drain variant '{Fname}': best-leg-evading relL2 {worst:.1%} < kill margin"


def test_drain_refB_kills_the_passives(refs):
    _, _, d = refs
    t, I = d["LOAM_B_t"], d["LOAM_B_I"]
    S, dth = float(d["LOAM_Sdes"]), float(d["LOAM_dtheta"])
    z0, kk = throttle_params(dth)
    imax = float(d["LOAM_R5_Imax"])
    for Fname, F in (("throttle", lambda z: F_throttle(z, z0, kk)),
                     ("planar", _planar), ("cyl", F_cylindrical)):
        clk = _clock(t, S, dth, F)
        e = min(rel_l2(clk, I), rel_l2(np.minimum(clk, imax), I))
        assert e >= 0.30, f"drain Ref B: passive '{Fname}' evades ({e:.1%}; measured >=33.6%)"
